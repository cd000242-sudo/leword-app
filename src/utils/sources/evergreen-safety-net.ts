// 하루 중 다양성을 위해 시간 기반 샤딩
export function getEvergreenSafetyNetSeeds(count: number = 40): string[] {
    const all = [...EVERGREEN_SEEDS];
    const shift = Math.floor(Date.now() / (6 * 60 * 60_000)) % all.length;
    const rotated = [...all.slice(shift), ...all.slice(0, shift)];
    return rotated.slice(0, Math.min(count, rotated.length));
}

