/**
 * 사용자 프로필 카테고리 → evergreen 시드 합집합
 * 카테고리당 최대 N개로 sampling (전체 풀 폭주 방지)
 */
export function getSeedsForUserCategories(
  categoryIds: BloggerCategoryId[],
  perCategory = 30,
): string[] {
  const result: string[] = [];
  for (const cid of categoryIds) {
    const seeds = CATEGORY_EVERGREEN_SEEDS[cid] || [];
    if (seeds.length <= perCategory) {
      result.push(...seeds);
    } else {
      const shuffled = seeds.slice().sort(() => Math.random() - 0.5);
      result.push(...shuffled.slice(0, perCategory));
    }
  }
  return Array.from(new Set(result));
}
